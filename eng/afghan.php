                        <h2>War with the USSR (1979-1989)</h2>
                        <p>In 1979, the USSR invaded Afghanistan, starting a 10-year war with local insurgents. The conflict killed about 1 million Afghans and 15,000 Soviet soldiers.</p>
                        <h2>Support for the Taliban (1996-2001)</h2>
                        <p>In 1996, the Taliban, an Islamic group, took power in Afghanistan. They provided sanctuary to many Islamic extremists, including al-Qaeda. In 2001, the United States and its allies launched a war against the Taliban and al-Qaeda.</p>
                        <h2>War with the United States (2001-2021)</h2>
                        <p>The war with the United States in 2001 was aimed at destroying the al-Qaeda terrorist group and overthrowing the Taliban regime. Over the 20 years of the conflict, more than 100,000 people have died, including about 2,400 US military personnel. In 2021, the United States and its allies announced the end of the war in Afghanistan and withdrew their troops from the country.</p>
                        <h2>Power and insurgency after the US withdrawal (2021-present)</h2>
                        <p>After the U.S. withdrew from Afghanistan in August 2021, the Taliban regained power in the country. The Afghan government was overthrown, and many local residents were left without protection. Since the beginning of the Taliban occupation of the country, there have been numerous uprisings and protests against the new government.</p>
                        <h2>Humanitarian crisis in Afghanistan</h2>
                        <p>After the Taliban occupation, a humanitarian crisis was declared in the country. Many local residents have been left without food and water, and there are problems with medical care and access to education. More than 500,000 people were forced to flee their homes, seeking refuge and protection from conflict and violence.</p>
                        <h2>International reaction</h2>
                        <p>The international community expresses serious concern about the situation in Afghanistan and calls on the Taliban to respect human rights and establish a stable government. Some countries are providing humanitarian aid and looking for ways to resolve the conflict diplomatically. However, the situation remains tense and unpredictable.</p>
                        <h2>Terrorist attacks after the US withdrawal</h2>
                        <p>Since the U.S. withdrawal from Afghanistan, terrorist groups, including ISIS-Khorasan and al-Qaeda, have carried out several attacks on local targets and foreign military bases. In November 2021, the largest terrorist attack in Afghanistan's history took place when a car bomb exploded during prayers at a mosque in Kandahar, killing more than 100 people and injuring many more.</p>

                        <h2>Political negotiations</h2>
                        <p>In December 2021, the Taliban and the Afghan government began negotiations on the future of the country and possible truce agreements. However, the talks have not yielded significant results so far, and the conflict continues.</p>

                        <h2>International evacuation</h2>
                        <p>During the Taliban's occupation, thousands of foreign nationals, including diplomats, journalists, and humanitarian workers, fled the country. In August 2021, after a rapid Taliban offensive, the United States and other countries organized a large-scale evacuation that lasted several weeks and ended successfully. Many people who remained in the country continue to struggle with the difficult situation and are trying to find a way to leave the country.</p>
